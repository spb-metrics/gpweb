require_once('../../lib/dompdf/dompdf_config.inc.php');
$dompdf = new DOMPDF();
$estilo='<style>
table.tbl{
	 border-collapse: collapse;
	 border: 1pt solid black; }
table.tbl TD{
	background-color:#a6a6a6;
	border: 1pt solid black; }	
table.tbl TH{
	 border-collapse: collapse;
	 border: 1pt solid black; 
	}	
table.limpa{
	 border-collapse: collapse;
	 border: 0pt solid black; }
</style>';
